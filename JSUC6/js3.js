//Subtração de dois números
let subtração = 8 - 3;
console.log(subtração); //Resultado: 5

//Subtração de números decimais
let decimal1 = 5.7;
let decimal2 = 2.3;
let subtracao = decimal1 - decimal2;
console.log(subtracao); // Results: 3.4

//Subtração de variáveis

let num1 = 10;
let num2 = 3;
let resultado = num1 - num2;
console.log(resultado); //Resultado: 7

//Divisão

let divisao = 10 / 2;
console.log(divisao); //Resultado: 5

//Divisão de números decimais

let decimaL1 = 5.5;
let decimaL2 = 2;
let divisaO = decimaL1 / decimaL2;
console.log(divisaO); // Resultado: 2.75